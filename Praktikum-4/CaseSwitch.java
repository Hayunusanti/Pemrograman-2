public class CaseSwitch {
	public static void main(String[] args) {
 String color = "Blue";
        switch(color){
    case "Blue":
    System.out.println("My favourite color is blue");
    break;
    case "Red":
    System.out.println("My favourite color is red");
    break;
    case "Green":
    System.out.println("My favourite color is green");
    break;
    case "Brown":
    System.out.println("My favourite color is brown");
    break;
        }
    }
}
